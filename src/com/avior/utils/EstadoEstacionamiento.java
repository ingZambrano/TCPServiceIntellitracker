package com.avior.utils;

/**
 * Representa el estado de un cierto dispositivo en la base 
 * de datos, en especifico del modo estacionamiento. No tiene
 * constructor pues se arma en el DAO.
 * @author jairo
 *
 */
public class EstadoEstacionamiento {
	private Long serial;
	private Integer status;
	private Double latitudFijada;
	private Double longitudFijada;
	private String telefonoContacto;
	
	
	
	
	@Override
	public String toString() {
		return "EstadoEstacionamiento [serial=" + serial + ", status=" + status
				+ ", latitudFijada=" + latitudFijada + ", longitudFijada="
				+ longitudFijada + ", telefonoContacto=" + telefonoContacto
				+ "]";
	}
	public String getTelefonoContacto() {
		return telefonoContacto;
	}
	public void setTelefonoContacto(String telefonoContacto) {
		this.telefonoContacto = telefonoContacto;
	}
	public Long getSerial() {
		return serial;
	}
	public void setSerial(Long serial) {
		this.serial = serial;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Double getLatitudFijada() {
		return latitudFijada;
	}
	public void setLatitudFijada(Double latitudFijada) {
		this.latitudFijada = latitudFijada;
	}
	public Double getLongitudFijada() {
		return longitudFijada;
	}
	public void setLongitudFijada(Double longitudFijada) {
		this.longitudFijada = longitudFijada;
	}	
	
}
